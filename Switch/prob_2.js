let colour = "yellow";
switch (colour){
    case "yellow":
        console.log("sunshine");
        break;
    case "pink":
        console.log("rose");
        break;
    case "black":
        console.log("black");
        break;
    case "blue":
        console.log("Today is Thursday");
        break;
    case "green":
        console.log("Today is Friday");
        break;
    default:
        console.log("Invalid day");
        break;
}